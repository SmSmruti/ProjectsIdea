package onlineVdoTelusco;

public class NestedloopDemo1 {
    public static void main(String[] args) {


        for (int i = 1; i <= 1; i++) {
            System.out.println(i + " ");
        }
        for (int j = 1; j <= 2; j++) {
            System.out.print(j + " ");
        }
        System.out.println();
        for (int k = 1; k <= 3; k++) {
            System.out.print(k + " ");
        }
        System.out.println();
        for (int l = 1; l <= 4; l++) {
            System.out.print(l + " ");
        }
        System.out.println();
        for (int m = 1; m <= 5; m++) {
            System.out.print(m + " ");
        }
        System.out.println();
        for (int n = 1; n <= 6; n++) {
            System.out.print(n + " ");
        }

    }
}
